package com.org.cygs.pojo;

import java.math.BigDecimal;

public class PieceworkServiceTotal {

	//工程名称,栋号,结算单位,分部,工程部位,价格, 已结工程量,已结金额
		private String prName;
		private String unitNumber;
		private String cuName;
		private String pName;
		private String psName;
		private BigDecimal price;
		private BigDecimal settledConstruction;
		private BigDecimal settledMoney;
		
		

		public String getPrName() {
			return prName;
		}
		public void setPrName(String prName) {
			this.prName = prName;
		}
		public String getUnitNumber() {
			return unitNumber;
		}
		public void setUnitNumber(String unitNumber) {
			this.unitNumber = unitNumber;
		}


		public String getCuName() {
			return cuName;
		}
		public void setCuName(String cuName) {
			this.cuName = cuName;
		}
		public String getpName() {
			return pName;
		}
		public void setpName(String pName) {
			this.pName = pName;
		}
		public String getPsName() {
			return psName;
		}
		public void setPsName(String psName) {
			this.psName = psName;
		}
		public BigDecimal getPrice() {
			return price;
		}
		public void setPrice(BigDecimal price) {
			this.price = price;
		}
		public BigDecimal getSettledConstruction() {
			return settledConstruction;
		}
		public void setSettledConstruction(BigDecimal settledConstruction) {
			this.settledConstruction = settledConstruction;
		}
		public BigDecimal getSettledMoney() {
			return settledMoney;
		}
		public void setSettledMoney(BigDecimal settledMoney) {
			this.settledMoney = settledMoney;
		}
		@Override
		public String toString() {
			return "PieceworkServiceTotal [prName=" + prName + ", unitNumber="
					+ unitNumber + ", cuName=" + cuName + ", pName=" + pName
					+ ", psName=" + psName + ", price=" + price
					+ ", settledConstruction=" + settledConstruction
					+ ", settledMoney=" + settledMoney + "]";
		}
		
		
}
