package com.org.cygs.pojo;

public class PermissionGroup {
	private String pgId;
	private String permissionGroupId;
	private String permissionGroupName;
	private String permissionGroupRemark;
	
	public String getPgId() {
		return pgId;
	}
	public void setPgId(String pgId) {
		this.pgId = pgId;
	}
	public String getPermissionGroupId() {
		return permissionGroupId;
	}
	public void setPermissionGroupId(String permissionGroupId) {
		this.permissionGroupId = permissionGroupId;
	}
	public String getPermissionGroupName() {
		return permissionGroupName;
	}
	public void setPermissionGroupName(String permissionGroupName) {
		this.permissionGroupName = permissionGroupName;
	}
	public String getPermissionGroupRemark() {
		return permissionGroupRemark;
	}
	public void setPermissionGroupRemark(String permissionGroupRemark) {
		this.permissionGroupRemark = permissionGroupRemark;
	}
}
