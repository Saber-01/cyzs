package com.org.cygs.pojo;

import java.util.Date;

import org.springframework.format.annotation.DateTimeFormat;

import com.fasterxml.jackson.annotation.JsonFormat;

public class SetTime {

	private String setId;
	private Integer sYear;
	private Integer sMonth;
	@DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss") 
	private Date sBeginTime;
	@DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss") 
	private Date sEndTime;
	@DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss") 
	private Date submitBeginTime;
	@DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss") 
	private Date submitEndTime;
	private String remark;
	private String remark1;
	private Integer status0;
	private Integer status;
	public String getSetId() {
		return setId;
	}
	public void setSetId(String setId) {
		this.setId = setId;
	}
	public Integer getsYear() {
		return sYear;
	}
	public void setsYear(Integer sYear) {
		this.sYear = sYear;
	}
	public Integer getsMonth() {
		return sMonth;
	}
	public void setsMonth(Integer sMonth) {
		this.sMonth = sMonth;
	}
	@DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss")  
	@JsonFormat(pattern="yyyy-MM-dd HH:mm:ss",timezone = "GMT+8")
	public Date getsBeginTime() {
		return sBeginTime;
	}
	public void setsBeginTime(Date sBeginTime) {
		this.sBeginTime = sBeginTime;
	}
	@DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss")  
	@JsonFormat(pattern="yyyy-MM-dd HH:mm:ss",timezone = "GMT+8")
	public Date getsEndTime() {
		return sEndTime;
	}
	public void setsEndTime(Date sEndTime) {
		this.sEndTime = sEndTime;
	}
	@DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss")  
	@JsonFormat(pattern="yyyy-MM-dd HH:mm:ss",timezone = "GMT+8")
	public Date getSubmitBeginTime() {
		return submitBeginTime;
	}
	public void setSubmitBeginTime(Date submitBeginTime) {
		this.submitBeginTime = submitBeginTime;
	}
	@DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss")  
	@JsonFormat(pattern="yyyy-MM-dd HH:mm:ss",timezone = "GMT+8")
	public Date getSubmitEndTime() {
		return submitEndTime;
	}
	public void setSubmitEndTime(Date submitEndTime) {
		this.submitEndTime = submitEndTime;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getRemark1() {
		return remark1;
	}
	public void setRemark1(String remark1) {
		this.remark1 = remark1;
	}
	public Integer getStatus0() {
		return status0;
	}
	public void setStatus0(Integer status0) {
		this.status0 = status0;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	
	
}
