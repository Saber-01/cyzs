package com.org.cygs.pojo;

import java.math.BigDecimal;
import java.util.Date;


public class BudgetContent {
	private String bcId;
	private String bId;
	private String budgetName;
	private int serial;
	private BigDecimal account;
    private String uId;
    private String userName;
    private Date budgetTime;
    private String remark;
	public String getBcId() {
		return bcId;
	}
	public void setBcId(String bcId) {
		this.bcId = bcId;
	}
	public String getbId() {
		return bId;
	}
	public void setbId(String bId) {
		this.bId = bId;
	}
	public String getBudgetName() {
		return budgetName;
	}
	public void setBudgetName(String budgetName) {
		this.budgetName = budgetName;
	}
	public int getSerial() {
		return serial;
	}
	public void setSerial(int serial) {
		this.serial = serial;
	}
	public BigDecimal getAccount() {
		return account;
	}
	public void setAccount(BigDecimal account) {
		this.account = account;
	}
	public String getuId() {
		return uId;
	}
	public void setuId(String uId) {
		this.uId = uId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public Date getBudgetTime() {
		return budgetTime;
	}
	public void setBudgetTime(Date budgetTime) {
		this.budgetTime = budgetTime;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	@Override
	public String toString() {
		return "BudgetContent [bcId=" + bcId + ", bId=" + bId + ", budgetName="
				+ budgetName + ", serial=" + serial + ", account=" + account
				+ ", uId=" + uId + ", userName=" + userName + ", budgetTime="
				+ budgetTime + ", remark=" + remark + "]";
	} 
}
